import React, { useEffect, useState } from 'react'
import { Popover, Button, Toast } from 'components'
import { IconStore, IconAriseSearch } from '@ali/super-icon'
import { DemoBlock } from 'demos'

const directionList = [
  `top`,
  `topRight`,
  `rightTop`,
  `right`,
  `rightBottom`,
  `bottomRight`,
  `bottom`,
  `bottomLeft`,
  `leftBottom`,
  `left`,
  `leftTop`,
  `topLeft`,
] as const

type Union<T extends readonly string[]> = T[number]

export default () => {
  const [direction, setDirection] = useState<Union<typeof directionList>>('top')

  useEffect(() => {
    let current = 0

    const timer = window.setInterval(() => {
      if (current >= directionList.length - 1) {
        current = 0
      } else {
        current += 1
      }
      setDirection(directionList[current])
    }, 2000)

    return () => {
      window.clearInterval(timer)
    }
  }, [])

  return (
    <>
      <DemoBlock title='基本的气泡'>
        <Popover content='Hello World' placement={'right'} trigger='click'>
          <Button>touch me</Button>
        </Popover>
      </DemoBlock>

      <DemoBlock title='带菜单的气泡'>
        <Popover.Menu
          actions={[{ text: '菜单1' }, { text: '菜单2', disabled: true }]}
          onAction={node => Toast.show(`选择了 ${node.text}`)}
          placement='bottomLeft'
          trigger='click'
        >
          <Button>touch me</Button>
        </Popover.Menu>
      </DemoBlock>

      <DemoBlock title='带图标的菜单气泡'>
        <Popover.Menu
          actions={[
            { text: '菜单1', icon: <IconStore size={16} color='#030202' /> },
            { text: '菜单2', icon: <IconAriseSearch size={16} color='#030202' /> },
          ]}
          placement='topRight'
          onAction={node => Toast.show(`选择了 ${node.text}`)}
          trigger='click'
        >
          <Button>touch me</Button>
        </Popover.Menu>
      </DemoBlock>

      <DemoBlock title='深色背景'>
        <Popover
          content='Hello World'
          placement='right'
          mode='dark'
          trigger='click'
        >
          <Button>touch me</Button>
        </Popover>
      </DemoBlock>

      <DemoBlock title='多种位置'>
        <Popover
          key={direction}
          visible
          content='Popover'
          placement={direction}
        >
          <div
            style={{
              margin: '32px auto',
              background: 'var(--i-bg_gray_03)',
              height: 100,
              width: 100,
              display: 'flex',
              alignItems: 'center',
              justifyContent: 'center',
              borderRadius: 8,
            }}
          >
            {direction}
          </div>
        </Popover>
      </DemoBlock>
    </>
  )
}
